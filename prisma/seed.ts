async function createRolePermissions() {
  console.log('üîê Criando permiss√µes por role...');

  // Buscar todas as permiss√µes
  const allPermissions = await prisma.permission.findMany();
  const permissionMap = new Map(allPermissions.map(p => [p.key, p.id]));

  // Permiss√µes para USER
  const userPermissions = [
    'dashboard.user.view',
    'diagnostico.view',
    'diagnostico.create',
    'plano.view',
    'conquista.view',
    'relatorio.view',
    'notification.view',
    'config.view',
    'config.edit',
    'conteudo.view',
  ];

  // Permiss√µes para ADMIN
  const adminPermissions = [
    ...userPermissions,
    'dashboard.admin.view',
    'diagnostico.edit',
    'diagnostico.delete',
    'plano.create',
    'plano.edit',
    'plano.delete',
    'conquista.manage',
    'conquista.create',
    'conquista.edit',
    'conquista.delete',
    'user.view',
    'user.create',
    'user.edit',
    'user.delete',
    'user.manage',
    'relatorio.view',
    'relatorio.create',
    'relatorio.export',
    'notification.view',
    'notification.create',
    'notification.stats',
    'config.view',
    'config.edit',
    'questionario.view',
    'questionario.create',
    'questionario.edit',
    'conteudo.view',
    'conteudo.create',
    'conteudo.edit',
    'conteudo.delete',
    'conteudo.manage',
    // Auditoria e Seguran√ßa para Admin
    'auditoria.logs.view',
    'auditoria.logs.export',
    'auditoria.alerts.view',
    'auditoria.alerts.manage',
    'auditoria.compliance.view',
    'auditoria.compliance.export',
  ];

  // Permiss√µes para MASTER (todas)
  const masterPermissions = allPermissions.map(p => p.key);

  // Criar role_permissions
  const rolePermissions = [
    { role: 'user', permissions: userPermissions },
    { role: 'admin', permissions: adminPermissions },
    { role: 'master', permissions: masterPermissions },
  ];

  for (const { role, permissions } of rolePermissions) {
    for (const permKey of permissions) {
      const permissionId = permissionMap.get(permKey);
      if (permissionId) {
        await prisma.role_permission.upsert({
          where: {
            role_permission_id: {
              role: role as any,
              permission_id: permissionId,
            },
          },
          update: {},
          create: {
            role: role as any,
            permission_id: permissionId,
          },
        });
      }
    }
  }

  console.log('‚úÖ Permiss√µes por role criadas');
}

